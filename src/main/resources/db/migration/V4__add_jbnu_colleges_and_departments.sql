-- 6. 기존 데이터 매핑 (표준 SQL 서브쿼리 방식)
UPDATE `courses`
SET (department_id, college_id) = (
    SELECT d.id, d.college_id
    FROM `departments` d
    WHERE `courses`.department = d.name
)
WHERE EXISTS (
    SELECT 1 FROM `departments` d WHERE `courses`.department = d.name
);

